import ast
from collections import defaultdict

def encontrar_diccionarios_iguales(texto):
    # Extraemos todos los substrings que parecen diccionarios usando delimitadores {}
    posibles_diccionarios = []
    inicio = None
    for i, char in enumerate(texto):
        if char == '{':
            inicio = i
        elif char == '}' and inicio is not None:
            fragmento = texto[inicio:i+1]
            try:
                diccionario = ast.literal_eval(fragmento)
                posibles_diccionarios.append(diccionario)
            except:
                pass
            inicio = None

    # Guardamos los diccionarios únicos y sus posiciones
    vistos = defaultdict(list)
    for idx, dicc in enumerate(posibles_diccionarios):
        clave = frozenset(dicc.items())  # congelamos para que sea hashable
        vistos[clave].append((idx, dicc))

    # Buscamos claves con más de una ocurrencia
    duplicados = {k: v for k, v in vistos.items() if len(v) > 1}

    if duplicados:
        print("Se encontraron diccionarios duplicados:")
        for i, ((_, dicc), *resto) in enumerate(duplicados.values(), 1):
            print(f"\nDiccionario duplicado #{i}:")
            print(dicc)
            print("Ocurre en las posiciones:", [pos for pos, _ in [(_, dicc)] + resto])
    else:
        print("No se encontraron diccionarios duplicados.")

